def _execute_make_investment_daytrader(
    self: AgentContextProtocol,
    action: Dict[str, Any],
    participant: Dict[str, Any],
    session: Dict[str, Any],
    session_key: str
) -> Dict[str, Any]:
    """Execute make_investment action for DayTrader"""
    invest_price = action.get('invest_price')
    invest_decision_type = action.get('invest_decision_type')  # 'individual' or 'group'
    
    if invest_price is None:
        return {
            'success': False,
            'action': action,
            'error': 'Missing invest_price'
        }
    
    try:
        invest_price = float(invest_price)
    except (ValueError, TypeError):
        return {
            'success': False,
            'action': action,
            'error': f'Invalid invest_price: {invest_price}'
        }
    
    if invest_decision_type not in ['individual', 'group']:
        return {
            'success': False,
            'action': action,
            'error': f'Invalid invest_decision_type: {invest_decision_type}'
        }
    
    # Validate price range
    min_price = get_value_from_session_params(session, 'Session.Params.minTradePrice')
    max_price = get_value_from_session_params(session, 'Session.Params.maxTradePrice')
    min_price = float(min_price) if min_price is not None else 15.0
    max_price = float(max_price) if max_price is not None else 100.0
    
    if invest_price < min_price or invest_price > max_price:
        return {
            'success': False,
            'action': action,
            'error': f'Investment price must be between ${min_price} and ${max_price}'
        }
    
    # Check if participant has enough money
    exp_params = participant.get('experiment_params', {})
    current_money = exp_params.get('money', 0)
    
    if current_money < invest_price:
        return {
            'success': False,
            'action': action,
            'error': f'Insufficient funds. Need ${invest_price}, have ${current_money}'
        }
    
    # Store investment - use investment_history (consistent with submit_investment)
    # Initialize investment_history if it doesn't exist
    if 'investment_history' not in exp_params:
        exp_params['investment_history'] = []
    
    # Ensure investment_history is a list
    if not isinstance(exp_params['investment_history'], list):
        exp_params['investment_history'] = []
    
    # Create investment record (consistent with submit_investment format)
    investment_record = {
        'id': str(uuid.uuid4()),
        'investment_amount': invest_price,
        'investment_type': invest_decision_type,
        'timestamp': datetime.now().isoformat(),
        'money_before': current_money,
        'money_after': current_money - invest_price
    }
    
    # Add to investment history
    exp_params['investment_history'].append(investment_record)
    
    # Deduct money
    exp_params['money'] = current_money - invest_price
    
    participant['experiment_params'] = exp_params
    
    # Update participant in session
    participants = session.get('participants', [])
    for i, p in enumerate(participants):
        if p.get('id') == self.participant_id:
            participants[i] = participant
            break
    
    session['participants'] = participants
    self.sessions[session_key] = session
    
    # Recompute interface
    from routes.participant import update_participant_experiment_params
    update_participant_experiment_params(participant, session)
    
    # Broadcast update
    broadcast_participant_update(
        session_id=self.session_id,
        participants=participants,
        session_info=session,
        update_type='partial'
    )
    
    return {
        'success': True,
        'action': action,
        'investment_id': investment_record['id'],
        'investment': investment_record,
        'remaining_money': exp_params['money']
    }


