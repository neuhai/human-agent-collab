def _execute_get_essay_content_essayranking(
    self: AgentContextProtocol,
    action: Dict[str, Any],
    participant: Dict[str, Any],
    session: Dict[str, Any],
    session_key: str
) -> Dict[str, Any]:
    """Execute get_essay_content action for EssayRanking - reads PDF content"""
    essay_name = action.get('essay_name')
    essay_id = action.get('essay_id')  # Also support essay_id for backward compatibility
    
    if not essay_name and not essay_id:
        return {
            'success': False,
            'action': action,
            'error': 'Missing essay_name or essay_id'
        }
    
    # Find essay in session by name or id
    essays = session.get('essays', [])
    essay = None
    for e in essays:
        if essay_id and e.get('essay_id') == essay_id:
            essay = e
            break
        elif essay_name:
            # Try to match by title, original_filename, or filename (without extension)
            e_title = e.get('title', '').lower().strip()
            e_original = e.get('original_filename', '').lower().strip()
            e_filename = e.get('filename', '').replace('.pdf', '').lower().strip()
            essay_name_lower = essay_name.lower().strip()
            
            if (e_title == essay_name_lower or 
                e_original == essay_name_lower or 
                e_filename == essay_name_lower or
                e_title == essay_name_lower.replace('.pdf', '') or
                e_original == essay_name_lower.replace('.pdf', '')):
                essay = e
                break
    
    if not essay:
        return {
            'success': False,
            'action': action,
            'error': f'Essay not found: {essay_name or essay_id}. Available essays: {[e.get("title") or e.get("original_filename") for e in essays]}'
        }
    
    # Get essay_id for checking if already read
    found_essay_id = essay.get('essay_id')
    
    # Check if essay has already been read
    read_essays = participant.get('read_essays', {})
    if found_essay_id and found_essay_id in read_essays:
        # Essay already read, return the existing content
        existing_data = read_essays[found_essay_id]
        return {
            'success': True,
            'action': action,
            'essay_id': found_essay_id,
            'essay_name': essay.get('title', ''),
            'content': existing_data.get('content', '')[:5000] + '...' if len(existing_data.get('content', '')) > 5000 else existing_data.get('content', ''),
            'content_length': len(existing_data.get('content', '')),
            'message': f'Essay "{essay.get("title", found_essay_id)}" was already read previously. Content is available in your context.',
            'already_read': True
        }
    
    # Get file path
    filename = essay.get('filename')
    if not filename:
        return {
            'success': False,
            'action': action,
            'error': 'Essay filename not found'
        }
    
    # Read PDF file
    import os
    upload_dir = os.path.join(os.path.dirname(os.path.dirname(__file__)), 'uploads', 'essays')
    file_path = os.path.join(upload_dir, filename)
    
    if not os.path.exists(file_path):
        return {
            'success': False,
            'action': action,
            'error': f'Essay file not found: {filename}'
        }
    
    # Try to read PDF content
    try:
        # Try PyPDF2 first
        try:
            import PyPDF2
            with open(file_path, 'rb') as file:
                pdf_reader = PyPDF2.PdfReader(file)
                content = []
                for page_num, page in enumerate(pdf_reader.pages):
                    text = page.extract_text()
                    if text.strip():
                        content.append(f"--- Page {page_num + 1} ---\n{text}")
                essay_content = '\n\n'.join(content)
        except ImportError:
            # Fallback to pdfplumber
            try:
                import pdfplumber
                with pdfplumber.open(file_path) as pdf:
                    content = []
                    for page_num, page in enumerate(pdf.pages):
                        text = page.extract_text()
                        if text:
                            content.append(f"--- Page {page_num + 1} ---\n{text}")
                    essay_content = '\n\n'.join(content)
            except ImportError:
                # If no PDF library is available, return error
                return {
                    'success': False,
                    'action': action,
                    'error': 'PDF reading library not available. Please install PyPDF2 or pdfplumber.'
                }
    except Exception as e:
        return {
            'success': False,
            'action': action,
            'error': f'Error reading PDF: {str(e)}'
        }
    
    if not essay_content or not essay_content.strip():
        return {
            'success': False,
            'action': action,
            'error': 'Could not extract text from PDF'
        }
    
    # Store essay content in participant's memory/context for future reference
    # This allows the agent to remember what it read
    if 'read_essays' not in participant:
        participant['read_essays'] = {}
    
    # Use found_essay_id (which was set earlier when finding the essay)
    participant['read_essays'][found_essay_id] = {
        'essay_id': found_essay_id,
        'title': essay.get('title', ''),
        'content': essay_content,
        'read_at': datetime.now().isoformat()
    }
    
    # Update participant in session
    participants = session.get('participants', [])
    for i, p in enumerate(participants):
        if p.get('id') == self.participant_id:
            participants[i] = participant
            break
    
    session['participants'] = participants
    self.sessions[session_key] = session
    
    return {
        'success': True,
        'action': action,
        'essay_id': found_essay_id,
        'essay_name': essay.get('title', ''),
        'content': essay_content[:5000] + '...' if len(essay_content) > 5000 else essay_content,  # Truncate for response
        'content_length': len(essay_content),
        'message': f'Successfully read essay "{essay.get("title", found_essay_id)}" ({len(essay_content)} characters). The full content is now available in your context under <READ ESSAYS CONTENT> section.'
    }


def _execute_submit_ranking_essayranking(
    self: AgentContextProtocol,
    action: Dict[str, Any],
    participant: Dict[str, Any],
    session: Dict[str, Any],
    session_key: str
) -> Dict[str, Any]:
    """Execute submit_ranking action for EssayRanking"""
    rankings = action.get('rankings')
    
    if not rankings:
        return {
            'success': False,
            'action': action,
            'error': 'Missing rankings'
        }
    
    if not isinstance(rankings, list):
        return {
            'success': False,
            'action': action,
            'error': 'rankings must be a list'
        }
    
    # Validate rankings
    essay_ids = set()
    ranks = set()
    
    # Get available essays from session
    available_essays = session.get('essays', [])
    available_essay_ids = {e.get('essay_id') for e in available_essays if e.get('essay_id')}
    # Create mapping from essay names to essay_ids
    essay_name_to_id = {}
    for e in available_essays:
        essay_id = e.get('essay_id')
        if essay_id:
            # Map by title, original_filename, and filename
            title = e.get('title', '').lower().strip()
            original = e.get('original_filename', '').lower().strip()
            filename = e.get('filename', '').replace('.pdf', '').lower().strip()
            if title:
                essay_name_to_id[title] = essay_id
            if original:
                essay_name_to_id[original] = essay_id
            if filename:
                essay_name_to_id[filename] = essay_id
    
    for ranking in rankings:
        if not isinstance(ranking, dict):
            return {
                'success': False,
                'action': action,
                'error': 'Each ranking must be a dictionary'
            }
        
        # Support both essay_name and essay_id
        essay_name = ranking.get('essay_name')
        essay_id = ranking.get('essay_id')
        
        # Convert essay_name to essay_id if needed
        if essay_name and not essay_id:
            essay_name_lower = essay_name.lower().strip()
            essay_id = essay_name_to_id.get(essay_name_lower) or essay_name_to_id.get(essay_name_lower.replace('.pdf', ''))
            if not essay_id:
                return {
                    'success': False,
                    'action': action,
                    'error': f'Essay not found by name: {essay_name}. Available essays: {[e.get("title") or e.get("original_filename") for e in available_essays]}'
                }
        
        if not essay_id:
            return {
                'success': False,
                'action': action,
                'error': 'Missing essay_name or essay_id in ranking'
            }
        
        rank = ranking.get('rank')
        
        if rank is None:
            return {
                'success': False,
                'action': action,
                'error': 'Missing rank in ranking'
            }
        
        try:
            rank = int(rank)
        except (ValueError, TypeError):
            return {
                'success': False,
                'action': action,
                'error': f'Invalid rank: {rank}'
            }
        
        # Validate essay_id exists
        if essay_id not in available_essay_ids:
            return {
                'success': False,
                'action': action,
                'error': f'Essay not found: {essay_id}'
            }
        
        if essay_id in essay_ids:
            return {
                'success': False,
                'action': action,
                'error': f'Duplicate essay: {essay_id}'
            }
        
        if rank in ranks:
            return {
                'success': False,
                'action': action,
                'error': f'Duplicate rank: {rank}'
            }
        
        essay_ids.add(essay_id)
        ranks.add(rank)
    
    # Validate that all essays are ranked
    if len(essay_ids) != len(available_essay_ids):
        missing_essay_ids = available_essay_ids - essay_ids
        missing_essays = [e.get('title') or e.get('original_filename') for e in available_essays if e.get('essay_id') in missing_essay_ids]
        return {
            'success': False,
            'action': action,
            'error': f'Missing rankings for essays: {missing_essays}'
        }
    
    # Store rankings (convert essay_name to essay_id if needed, but keep both for reference)
    exp_params = participant.get('experiment_params', {})
    # Normalize rankings to use essay_id, and include essay_title for display
    normalized_rankings = []
    for ranking in rankings:
        normalized_ranking = ranking.copy()
        # If ranking has essay_name but not essay_id, find the essay_id
        if 'essay_name' in normalized_ranking and 'essay_id' not in normalized_ranking:
            essay_name = normalized_ranking.get('essay_name')
            essay_name_lower = essay_name.lower().strip()
            # Find essay_id from available essays
            for e in available_essays:
                e_title = e.get('title', '').lower().strip()
                e_original = e.get('original_filename', '').lower().strip()
                if (e_title == essay_name_lower or 
                    e_original == essay_name_lower or
                    e_title == essay_name_lower.replace('.pdf', '') or
                    e_original == essay_name_lower.replace('.pdf', '')):
                    normalized_ranking['essay_id'] = e.get('essay_id')
                    # Also store essay_title for display
                    normalized_ranking['essay_title'] = e.get('title') or e.get('original_filename') or essay_name
                    break
        # Ensure essay_id is present
        if 'essay_id' not in normalized_ranking:
            # Try to find by essay_id if it was provided directly
            essay_id = ranking.get('essay_id')
            if essay_id:
                normalized_ranking['essay_id'] = essay_id
                # Find essay_title from available essays
                for e in available_essays:
                    if e.get('essay_id') == essay_id:
                        normalized_ranking['essay_title'] = e.get('title') or e.get('original_filename') or essay_id
                        break
        # If essay_id is present but essay_title is missing, find it from available essays
        if 'essay_id' in normalized_ranking and 'essay_title' not in normalized_ranking:
            essay_id = normalized_ranking.get('essay_id')
            for e in available_essays:
                if e.get('essay_id') == essay_id:
                    normalized_ranking['essay_title'] = e.get('title') or e.get('original_filename') or essay_id
                    break
        normalized_rankings.append(normalized_ranking)
    
    exp_params['rankings'] = normalized_rankings
    participant['experiment_params'] = exp_params
    
    # Update participant in session
    participants = session.get('participants', [])
    for i, p in enumerate(participants):
        if p.get('id') == self.participant_id:
            participants[i] = participant
            break
    
    session['participants'] = participants
    self.sessions[session_key] = session
    
    # Recompute interface
    from routes.participant import update_participant_experiment_params
    update_participant_experiment_params(participant, session)
    
    # Broadcast update
    broadcast_participant_update(
        session_id=self.session_id,
        participants=participants,
        session_info=session,
        update_type='partial'
    )
    
    return {
        'success': True,
        'action': action,
        'rankings_count': len(rankings)
    }


