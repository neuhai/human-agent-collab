def _execute_propose_trade_offer_shapefactory(
    self: AgentContextProtocol,
    action: Dict[str, Any],
    participant: Dict[str, Any],
    session: Dict[str, Any],
    session_key: str
) -> Dict[str, Any]:
    """Execute propose_trade_offer action for ShapeFactory"""
    offer_type = action.get('offer_type')  # 'buy' or 'sell'
    shape = action.get('shape')
    price_per_unit = action.get('price_per_unit')
    target_participant_code = action.get('target_participant')
    
    if offer_type not in ['buy', 'sell']:
        return {
            'success': False,
            'action': action,
            'error': f'Invalid offer_type: {offer_type}'
        }
    
    if not shape:
        return {
            'success': False,
            'action': action,
            'error': 'Missing shape'
        }
    
    if price_per_unit is None:
        return {
            'success': False,
            'action': action,
            'error': 'Missing price_per_unit'
        }
    
    try:
        price = float(price_per_unit)
    except (ValueError, TypeError):
        return {
            'success': False,
            'action': action,
            'error': f'Invalid price_per_unit: {price_per_unit}'
        }
    
    if not target_participant_code:
        return {
            'success': False,
            'action': action,
            'error': 'Missing target_participant'
        }
    
    # Find target participant by name/code
    participants = session.get('participants', [])
    target_participant = None
    for p in participants:
        p_name = p.get('name') or p.get('participant_name')
        if p_name and p_name.lower() == target_participant_code.lower():
            target_participant = p
            break
    
    if not target_participant:
        return {
            'success': False,
            'action': action,
            'error': f'Target participant not found: {target_participant_code}'
        }
    
    # Validate trade constraints
    if offer_type == 'sell':
        # Check if seller has the shape in inventory
        exp_params = participant.get('experiment_params', {})
        inventory = exp_params.get('inventory', [])
        if shape not in inventory:
            return {
                'success': False,
                'action': action,
                'error': f'You do not have {shape} in your inventory'
            }
        
        # Check if buyer has enough money
        target_exp_params = target_participant.get('experiment_params', {})
        buyer_money = target_exp_params.get('money', 0)
        if buyer_money < price:
            return {
                'success': False,
                'action': action,
                'error': 'Buyer does not have enough money'
            }
    elif offer_type == 'buy':
        # Check if buyer has enough money
        exp_params = participant.get('experiment_params', {})
        buyer_money = exp_params.get('money', 0)
        if buyer_money < price:
            return {
                'success': False,
                'action': action,
                'error': 'You do not have enough money'
            }
    
    # Create trade offer
    if 'pending_offers' not in session:
        session['pending_offers'] = []
    
    offer_id = str(uuid.uuid4())
    offer = {
        'id': offer_id,
        'from': self.participant_id,
        'to': target_participant.get('id'),
        'offer_type': offer_type,
        'item_type': 'shape',
        'quantity': 1,
        'price': price,
        'status': 'pending',
        'timestamp': datetime.now().isoformat(),
        'trade_item': shape,
        'shape': shape  # Backward compatibility
    }
    
    session['pending_offers'].append(offer)
    self.sessions[session_key] = session
    
    # Broadcast update
    broadcast_participant_update(
        session_id=self.session_id,
        participants=participants,
        session_info=session,
        update_type='trade_update'
    )
    
    return {
        'success': True,
        'action': action,
        'offer_id': offer_id
    }


def _execute_cancel_trade_offer_shapefactory(
    self: AgentContextProtocol,
    action: Dict[str, Any],
    participant: Dict[str, Any],
    session: Dict[str, Any],
    session_key: str
) -> Dict[str, Any]:
    """Execute cancel_trade_offer action for ShapeFactory"""
    transaction_id = action.get('transaction_id')
    
    if not transaction_id:
        return {
            'success': False,
            'action': action,
            'error': 'Missing transaction_id'
        }
    
    pending_offers = session.get('pending_offers', [])
    offer = None
    offer_index = None
    
    for i, o in enumerate(pending_offers):
        if o.get('id') == transaction_id:
            offer = o
            offer_index = i
            break
    
    if not offer:
        return {
            'success': False,
            'action': action,
            'error': f'Offer not found: {transaction_id}'
        }
    
    if offer.get('from') != self.participant_id:
        return {
            'success': False,
            'action': action,
            'error': 'You are not the sender of this offer'
        }
    
    if offer.get('status') != 'pending':
        return {
            'success': False,
            'action': action,
            'error': 'Offer is no longer pending'
        }
    
    # Create cancelled trade record
    if 'completed_trades' not in session:
        session['completed_trades'] = []
    
    cancelled_trade = {
        'id': str(uuid.uuid4()),
        'from': offer.get('from'),
        'to': offer.get('to'),
        'offer_type': offer.get('offer_type'),
        'item_type': offer.get('item_type', 'shape'),
        'quantity': offer.get('quantity', 1),
        'price': offer.get('price'),
        'status': 'cancelled',
        'timestamp': datetime.now().isoformat()
    }
    
    trade_item = offer.get('trade_item') or offer.get('shape')
    if trade_item:
        cancelled_trade['trade_item'] = trade_item
        if offer.get('item_type', 'shape') == 'shape':
            cancelled_trade['shape'] = trade_item
    
    session['completed_trades'].append(cancelled_trade)
    
    # Remove from pending offers
    pending_offers.pop(offer_index)
    offer['status'] = 'cancelled'
    session['pending_offers'] = pending_offers
    self.sessions[session_key] = session
    
    # Broadcast update
    participants = session.get('participants', [])
    broadcast_participant_update(
        session_id=self.session_id,
        participants=participants,
        session_info=session,
        update_type='trade_update'
    )
    
    return {
        'success': True,
        'action': action,
        'offer_id': transaction_id
    }


def _execute_trade_response_shapefactory(
    self: AgentContextProtocol,
    action: Dict[str, Any],
    participant: Dict[str, Any],
    session: Dict[str, Any],
    session_key: str
) -> Dict[str, Any]:
    """Execute trade_response action (accept/decline) for ShapeFactory"""
    transaction_id = action.get('transaction_id')
    response_type = action.get('response_type')  # 'accept' or 'decline'
    
    if not transaction_id:
        return {
            'success': False,
            'action': action,
            'error': 'Missing transaction_id'
        }
    
    if response_type not in ['accept', 'decline']:
        return {
            'success': False,
            'action': action,
            'error': f'Invalid response_type: {response_type}'
        }
    
    pending_offers = session.get('pending_offers', [])
    offer = None
    offer_index = None
    
    for i, o in enumerate(pending_offers):
        if o.get('id') == transaction_id:
            offer = o
            offer_index = i
            break
    
    if not offer:
        return {
            'success': False,
            'action': action,
            'error': f'Offer not found: {transaction_id}'
        }
    
    if offer.get('to') != self.participant_id:
        return {
            'success': False,
            'action': action,
            'error': 'You are not the recipient of this offer'
        }
    
    if offer.get('status') != 'pending':
        return {
            'success': False,
            'action': action,
            'error': 'Offer is no longer pending'
        }
    
    participants = session.get('participants', [])
    from_participant = None
    to_participant = participant
    
    for p in participants:
        if p.get('id') == offer.get('from'):
            from_participant = p
            break
    
    if not from_participant:
        return {
            'success': False,
            'action': action,
            'error': 'Sender participant not found'
        }
    
    if response_type == 'accept':
        # Execute the trade (same logic as in participant.py)
        trade_item = offer.get('trade_item') or offer.get('shape')
        item_type = offer.get('item_type', 'shape')
        quantity = offer.get('quantity', 1)
        price = offer.get('price')
        offer_type = offer.get('offer_type')
        
        from_exp_params = from_participant.get('experiment_params', {})
        to_exp_params = to_participant.get('experiment_params', {})
        
        # Validate seller has item
        if trade_item and item_type != 'price_only':
            seller_inventory = None
            if offer_type == 'sell':
                seller_inventory = from_exp_params.get('inventory', [])
            elif offer_type == 'buy':
                seller_inventory = to_exp_params.get('inventory', [])
            
            if seller_inventory is not None:
                if item_type == 'shape':
                    item_count = seller_inventory.count(trade_item)
                    if item_count < quantity:
                        return {
                            'success': False,
                            'action': action,
                            'error': f'Seller does not have {quantity}x {trade_item} in inventory'
                        }
        
        # Validate buyer has money
        buyer_money = None
        if offer_type == 'sell':
            buyer_money = to_exp_params.get('money', 0)
        elif offer_type == 'buy':
            buyer_money = from_exp_params.get('money', 0)
        
        if buyer_money is not None and buyer_money < price:
            return {
                'success': False,
                'action': action,
                'error': f'Buyer does not have enough money (has ${buyer_money}, needs ${price})'
            }
        
        # Execute trade
        def remove_item_from_inventory(inventory, item, item_type, qty=1):
            if not isinstance(inventory, list):
                return inventory
            new_inventory = inventory.copy()
            for _ in range(qty):
                if item_type == 'shape':
                    if item in new_inventory:
                        new_inventory.remove(item)
                else:
                    for i, inv_item in enumerate(new_inventory):
                        if isinstance(inv_item, dict):
                            if inv_item.get('type') == item_type and inv_item.get('value') == item:
                                new_inventory.pop(i)
                                break
                        elif inv_item == item:
                            new_inventory.pop(i)
                            break
            return new_inventory
        
        def add_item_to_inventory(inventory, item, item_type, qty=1):
            if not isinstance(inventory, list):
                inventory = []
            new_inventory = inventory.copy()
            for _ in range(qty):
                if item_type == 'shape':
                    new_inventory.append(item)
                else:
                    new_inventory.append({'type': item_type, 'value': item})
            return new_inventory
        
        if offer_type == 'sell':
            if trade_item and item_type != 'price_only':
                from_inventory = from_exp_params.get('inventory', [])
                from_exp_params['inventory'] = remove_item_from_inventory(from_inventory, trade_item, item_type, quantity)
                
                to_inventory = to_exp_params.get('inventory', [])
                to_exp_params['inventory'] = add_item_to_inventory(to_inventory, trade_item, item_type, quantity)
            
            from_money = from_exp_params.get('money', 0)
            from_exp_params['money'] = from_money + price
            
            to_money = to_exp_params.get('money', 0)
            to_exp_params['money'] = to_money - price
            
        elif offer_type == 'buy':
            if trade_item and item_type != 'price_only':
                to_inventory = to_exp_params.get('inventory', [])
                to_exp_params['inventory'] = remove_item_from_inventory(to_inventory, trade_item, item_type, quantity)
                
                from_inventory = from_exp_params.get('inventory', [])
                from_exp_params['inventory'] = add_item_to_inventory(from_inventory, trade_item, item_type, quantity)
            
            to_money = to_exp_params.get('money', 0)
            to_exp_params['money'] = to_money + price
            
            from_money = from_exp_params.get('money', 0)
            from_exp_params['money'] = from_money - price
        
        from_participant['experiment_params'] = from_exp_params
        to_participant['experiment_params'] = to_exp_params
        
        # Recompute interfaces
        from routes.participant import update_participant_experiment_params
        update_participant_experiment_params(from_participant, session)
        update_participant_experiment_params(to_participant, session)
        
        # Create completed trade record
        if 'completed_trades' not in session:
            session['completed_trades'] = []
        
        completed_trade = {
            'id': str(uuid.uuid4()),
            'from': offer.get('from'),
            'to': offer.get('to'),
            'offer_type': offer_type,
            'item_type': item_type,
            'quantity': quantity,
            'price': price,
            'status': 'completed',
            'timestamp': datetime.now().isoformat()
        }
        
        if trade_item:
            completed_trade['trade_item'] = trade_item
            if item_type == 'shape':
                completed_trade['shape'] = trade_item
        
        session['completed_trades'].append(completed_trade)
        
        # Remove from pending offers
        pending_offers.pop(offer_index)
        offer['status'] = 'accepted'
    else:  # decline
        # Create declined trade record
        if 'completed_trades' not in session:
            session['completed_trades'] = []
        
        declined_trade = {
            'id': str(uuid.uuid4()),
            'from': offer.get('from'),
            'to': offer.get('to'),
            'offer_type': offer.get('offer_type'),
            'item_type': offer.get('item_type', 'shape'),
            'quantity': offer.get('quantity', 1),
            'price': offer.get('price'),
            'status': 'declined',
            'timestamp': datetime.now().isoformat()
        }
        
        trade_item = offer.get('trade_item') or offer.get('shape')
        if trade_item:
            declined_trade['trade_item'] = trade_item
            if offer.get('item_type', 'shape') == 'shape':
                declined_trade['shape'] = trade_item
        
        session['completed_trades'].append(declined_trade)
        
        # Remove from pending offers
        pending_offers.pop(offer_index)
        offer['status'] = 'declined'
    
    # Update session
    session['pending_offers'] = pending_offers
    session['participants'] = participants
    self.sessions[session_key] = session
    
    # Broadcast update
    broadcast_participant_update(
        session_id=self.session_id,
        participants=participants,
        session_info=session,
        update_type='trade_update'
    )
    
    return {
        'success': True,
        'action': action,
        'response': response_type
    }


def _execute_produce_shape_shapefactory(
    self: AgentContextProtocol,
    action: Dict[str, Any],
    participant: Dict[str, Any],
    session: Dict[str, Any],
    session_key: str
) -> Dict[str, Any]:
    """Execute produce_shape action for ShapeFactory"""
    shape = action.get('shape')
    quantity = action.get('quantity', 1)
    
    if not shape:
        return {
            'success': False,
            'action': action,
            'error': 'Missing shape'
        }
    
    try:
        quantity = int(quantity)
        if quantity <= 0:
            return {
                'success': False,
                'action': action,
                'error': 'Quantity must be positive'
            }
    except (ValueError, TypeError):
        return {
            'success': False,
            'action': action,
            'error': f'Invalid quantity: {quantity}'
        }
    
    # Execute production for each shape
    results = []
    for _ in range(quantity):
        result = start_production(participant, session, shape)
        if not result.get('success'):
            return {
                'success': False,
                'action': action,
                'error': result.get('error', 'Production failed'),
                'partial_results': results
            }
        results.append(result)
    
    # Update participant in session
    participants = session.get('participants', [])
    for i, p in enumerate(participants):
        if p.get('id') == self.participant_id:
            participants[i] = participant
            break
    
    session['participants'] = participants
    self.sessions[session_key] = session
    
    # Recompute interface
    from routes.participant import update_participant_experiment_params
    update_participant_experiment_params(participant, session)
    
    # Broadcast update
    broadcast_participant_update(
        session_id=self.session_id,
        participants=participants,
        session_info=session,
        update_type='partial'
    )
    
    return {
        'success': True,
        'action': action,
        'results': results
    }


def _execute_fulfill_order_shapefactory(
    self: AgentContextProtocol,
    action: Dict[str, Any],
    participant: Dict[str, Any],
    session: Dict[str, Any],
    session_key: str
) -> Dict[str, Any]:
    """Execute fulfill_order action for ShapeFactory"""
    order_indices = action.get('order_indices')
    
    if not order_indices:
        return {
            'success': False,
            'action': action,
            'error': 'Missing order_indices'
        }
    
    if not isinstance(order_indices, list):
        return {
            'success': False,
            'action': action,
            'error': 'order_indices must be a list'
        }
    
    exp_params = participant.get('experiment_params', {})
    inventory = exp_params.get('inventory', [])
    tasks = exp_params.get('tasks', [])
    
    if not isinstance(inventory, list):
        inventory = []
    if not isinstance(tasks, list):
        tasks = []
    
    # Validate indices
    for idx in order_indices:
        try:
            idx = int(idx)
            if idx < 0 or idx >= len(tasks):
                return {
                    'success': False,
                    'action': action,
                    'error': f'Invalid order index: {idx}'
                }
        except (ValueError, TypeError):
            return {
                'success': False,
                'action': action,
                'error': f'Invalid order index: {idx}'
            }
    
    # Get shapes to fulfill
    shapes_to_fulfill = [tasks[idx] for idx in order_indices]
    
    # Check if participant has all required shapes
    inventory_copy = inventory.copy()
    for shape in shapes_to_fulfill:
        if shape not in inventory_copy:
            return {
                'success': False,
                'action': action,
                'error': f'Shape {shape} not in inventory'
            }
        inventory_copy.remove(shape)
    
    # Get incentive money
    incentive_money = get_value_from_session_params(session, 'Session.Params.incentiveMoney')
    incentive_money = int(incentive_money) if incentive_money is not None else 60
    
    # Remove shapes from inventory
    for shape in shapes_to_fulfill:
        inventory.remove(shape)
    
    # Remove fulfilled orders from tasks
    # Remove in reverse order to maintain indices
    for idx in sorted(order_indices, reverse=True):
        if idx < len(tasks):
            tasks.pop(idx)
    
    # Add incentive money
    current_money = exp_params.get('money', 0)
    exp_params['money'] = current_money + (incentive_money * len(shapes_to_fulfill))
    
    # Update order progress
    current_order_progress = exp_params.get('order_progress', 0)
    exp_params['order_progress'] = current_order_progress + len(shapes_to_fulfill)
    
    # Update participant
    exp_params['inventory'] = inventory
    exp_params['tasks'] = tasks
    participant['experiment_params'] = exp_params
    
    # Update participant in session
    participants = session.get('participants', [])
    for i, p in enumerate(participants):
        if p.get('id') == self.participant_id:
            participants[i] = participant
            break
    
    session['participants'] = participants
    self.sessions[session_key] = session
    
    # Recompute interface
    from routes.participant import update_participant_experiment_params
    update_participant_experiment_params(participant, session)
    
    # Broadcast update
    broadcast_participant_update(
        session_id=self.session_id,
        participants=participants,
        session_info=session,
        update_type='partial'
    )
    
    return {
        'success': True,
        'action': action,
        'fulfilled_count': len(shapes_to_fulfill),
        'incentive_money': incentive_money * len(shapes_to_fulfill)
    }


