"""
Agent Context Protocol: Converts agent-generated actions into system behaviors.

This module is responsible for:
1. Parsing agent-generated actions (from LLM responses)
2. Converting them into system API calls based on experiment type
3. Executing these actions through the appropriate routes/handlers

The system supports customizable action handlers for different experiment types.
"""

